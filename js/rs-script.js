function copyHtmlToClipboard() {
	const element = document.getElementById('copyToClipboard');
	const textContent = element.innerText || element.textContent;

	// Создаем временный элемент textarea
	const tempTextarea = document.createElement('textarea');
	tempTextarea.value = textContent;

	// Добавляем временный textarea на страницу (вне видимости пользователя)
	document.body.appendChild(tempTextarea);

	// Выделяем текст в textarea и копируем его в буфер обмена
	tempTextarea.select();
	document.execCommand('copy');

	// Удаляем временный textarea
	document.body.removeChild(tempTextarea);

	// Показать мини-уведомление
	showNotification('Скопировано');
}
function showNotification(message) {
	// Создаем элемент уведомления
	const notification = document.createElement('div');
	notification.className = 'notification';
	notification.innerText = message;

	// Добавляем уведомление на страницу
	document.body.appendChild(notification);

	// Используем requestAnimationFrame для добавления класса .show
	requestAnimationFrame(() => {
		notification.classList.add('show');
	});
	// Удаляем уведомление через 3 секунды
	setTimeout(() => {
		notification.classList.add('fade');
		setTimeout(() => document.body.removeChild(notification), 500);
	}, 3000);
}
