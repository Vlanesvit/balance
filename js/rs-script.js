function duplicateTextForAnimation(selector) {
	document.querySelectorAll(selector).forEach(element => {
		element.classList.remove('marquee-text');

		const textContent = Array.from(element.childNodes)
			.filter(node => node.nodeType === Node.TEXT_NODE && node.textContent.trim())
			.map(node => node.textContent.trim())
			.join(' ');

		if (textContent) {
			const spacingValue = Math.max(textContent.length * 0.7, 7);
			element.style.setProperty('--spacing', `${spacingValue}em`);

			// Определяем длину текста
			const textLength = textContent.length;

			// Устанавливаем значения для --start и --end
			const startValue = 0; // Начинаем с 0
			const endValue = Math.max(textLength * 0.7, 7); // Увеличиваем значение в зависимости от длины текста
			element.style.setProperty('--start', `${startValue}em`);
			element.style.setProperty('--end', `${endValue}em`);

			// Создаем новый HTML с анимационными обертками
			element.innerHTML = `
                <span class="rs-btn__text">${textContent}</span>
                <span aria-hidden class="rs-btn__marquee">${textContent}</span>
            `;

			// Запускаем анимацию, добавляя класс
			element.classList.add('marquee-text');
		}
	});
}
